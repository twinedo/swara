import { mkdir, writeFile } from "node:fs/promises";
import path from "node:path";
import process from "node:process";
import { fileURLToPath } from "node:url";

import openapiTS from "openapi-typescript";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const outputDir = path.resolve(__dirname, "../src/generated");
const outputFile = path.join(outputDir, "openapi.d.ts");
const source =
  process.argv[2] ??
  process.env.SWARA_OPENAPI_URL ??
  "http://127.0.0.1:8787/api-docs/openapi.json";

const generated = await openapiTS(source, {
  alphabetize: true,
  enum: true,
});

await mkdir(outputDir, { recursive: true });
await writeFile(
  outputFile,
  [
    "// Generated by packages/types/scripts/generate-openapi-types.mjs",
    `// Source: ${source}`,
    "",
    generated,
  ].join("\n"),
);

console.log(`Wrote ${outputFile}`);
